// Assignment Operators

let x = 5;

// Addition Assignment

x +=1; // x = x + 1

// Subtraction Assignment 
 x -= 1; // x = x - 1

 // Multiplication Assignment

 x *= 1; // x = x * 1

 //Division Assignment

 x /= 1; // x = x / 1

 // Remainder (or Modulus) Assignment

 x %= 1; // x = x % 1

 // Exponential Assignment
 x **= 1; // x = x ** 1


